<p>That being said, the three of us have graduated. We spent a lot of time and effort bringing GetchaBooks to as many schools as we could, but now that we're no longer students we don't have as much time to focus on expansion.</p>

<p>To help as many students save on textbooks as possible, we came up with an idea: we might not be able to bring GetchaBooks to your school, but with a little work, you can! We are proud to announce that we are releasing <a href="https://github.com/getchabooks/getchabooks">the entire GetchaBooks source code on GitHub</a>.</p>

<p>Our loyal GetchaBooks users have nothing to worry about. We'll keep maintaining GetchaBooks.com for the forseeable future, and this announcement shouldn't affect that at all.</p>

<p>If you're a student with web development chops, you definitely want to check out our <a href="https://github.com/getchabooks/getchabooks">GitHub repo</a>. We want you to take our code and build your own take on GetchaBooks, specifically for your school or region, to help even more students save money. As a bonus, you can make money for yourself through online bookstore affiliate programs, or you can donate the money to charity.</p><p>
If you have any questions, feel free to <a href="mailto:feedback@getchabooks.com">get in touch</a>. Have fun!</p>

